#include <stdio.h>

int main()
{
    int weekNumber;
    weekNumber = 1;

    if (weekNumber == 1)
    {
        printf("Monday");
    }
    else if (weekNumber == 2)
    {
        printf("Tuesday");
    }
    else if (weekNumber == 3)
    {
        printf("Wednesday");
    }
    else if (weekNumber == 4)
    {
        printf("Thursday");
    }
    else if (weekNumber == 5)
    {
        printf("Friday");
    }
    else if (weekNumber == 6)
    {
        printf("Saturday");
    }
    else if (weekNumber == 7)
    {
        printf("Sunday");
    }

    return 0;
}